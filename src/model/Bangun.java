package model;

public class Bangun {
	private String nama;
	private int tinggi;
	
	public Bangun(String nama, int tinggi) {
		this.nama = nama;
		this.tinggi = tinggi;
	}
	public String getNama() {
		return nama;
	}
	public void setNama() {
		this.nama = nama;
	} 
	public int getTinggi() {
		return tinggi;
	}
	public void setTinggi(int tinggi) {
        this.tinggi = tinggi;
    }
	public double getLuas() {
		return tinggi;
	}

}
